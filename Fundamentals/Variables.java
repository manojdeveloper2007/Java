public class Variables {
    public static void main(String[] args) {
        // Integers

        int num=10;
        byte byt=127;
        short sh=32100;
        long lg=1288900L;

        // Decimal

        float pi=3.15F;
        double avg=12.52999;

        // Character

        char grade='S';

        // Boolean
    
        boolean isHuman=true;
        boolean isAnimal=false;

        // Display the Variables

        System.out.println("Numbers : "+num+" "+sh+" "+byt+" "+lg);
        System.out.println("Decimal : "+pi+" "+avg);
        System.out.println("Characters : "+grade);
        System.out.println("Boolean : "+isHuman+" "+isAnimal);

    }
}
